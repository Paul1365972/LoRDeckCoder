package io.github.paul1365972.lordeckcoder.exceptions;

public class LoRUnsupportedFormatException extends LoRCodingException {
	
	public LoRUnsupportedFormatException() {
		super();
	}
	
	public LoRUnsupportedFormatException(String message) {
		super(message);
	}
	
	public LoRUnsupportedFormatException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public LoRUnsupportedFormatException(Throwable cause) {
		super(cause);
	}
	
}
